
'''
Enter an integer between 1 and 23, inclusive.
Prompt user if number falls outside the boundary of acceptable values(negative, 0, 24 and up).
Buid a pyramid of height = base = integer.
'''

num = int(raw_input("Enter height: "))
while (num < 1) or (num > 23):
    num = int(raw_input("Retry. Enter height: "))
              
for i in range(0,num):
    sharp = i + 1
    space = num-sharp
    print (space*"*"+ sharp*"#"+"  " + sharp*"#"+space*"*")
    
    
